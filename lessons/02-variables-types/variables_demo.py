# Variables and Data Types Demo

# Creating variables
name = "Alice"
age = 25
height = 5.6
is_student = True

# Using variables
print("Name:", name)
print("Age:", age)
print("Height:", height, "feet")
print("Is student:", is_student)

print("\n--- Data Types ---")
# Different data types
message = "Hello, Python!"
score = 100
price = 19.99
is_sunny = True

# Check types
print("Message type:", type(message))
print("Score type:", type(score))
print("Price type:", type(price))
print("Is sunny type:", type(is_sunny))

print("\n--- Working with Variables ---")
# Math with variables
x = 5
y = 3
sum_result = x + y
print("Sum:", sum_result)

# String operations
first_name = "John"
last_name = "Doe"
full_name = first_name + " " + last_name
print("Full name:", full_name)
